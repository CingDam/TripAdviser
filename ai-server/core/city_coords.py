def get_city_coords(city_name: str) -> tuple[float, float] | None:
    """도시명으로 중심 좌표 반환. 매핑에 없으면 None."""
    if not city_name:
        return None
    # 정확히 일치
    coords = CITY_COORDS.get(city_name.strip())
    if coords:
        return coords
    # 부분 일치 — "도쿄 신주쿠" → "도쿄"
    for key, val in CITY_COORDS.items():
        if key in city_name or city_name in key:
            return val
    return None
